// Add metabox
function tna_custom_metabox($post)
{
    $template = get_post_meta($post->ID, '_wp_page_template', true);
    if ('home-with-external-links.php' == $template || 'business-plan.php' == $template) {
        global $meta_box;
        add_meta_box($meta_box['id'], $meta_box['title'], 'tna_show_box', $meta_box['page'], $meta_box['context'], $meta_box['priority']);
    }
//About us
    if ('about-us.php' == $template) {
        global $meta_box_about;
        add_meta_box($meta_box_about['id'], $meta_box_about['title'], 'tna_about_us_action', $meta_box_about['page'], $meta_box_about['context'], $meta_box_about['priority']);
    }

//Meeting Minutes
    if ($template == 'meeting-landing.php') {
        global $meta_box_meeting, $meta_box_meeting2;
        add_meta_box($meta_box_meeting['id'], $meta_box_meeting['title'], 'tna_meeting_minutes_action', $meta_box_meeting['page'], $meta_box_meeting['context'], $meta_box_meeting['priority']);
        add_meta_box($meta_box_meeting2['id'], $meta_box_meeting2['title'], 'tna_meeting_minutes_action2', $meta_box_meeting2['page'], $meta_box_meeting2['context'], $meta_box_meeting2['priority']);
    }

}
add_action('add_meta_boxes_page', 'tna_custom_metabox');


// Callback function to show fields in meta box
function tna_show_box() {
    global $meta_box, $post;
// Use nonce for verification
    echo '<input type="hidden" name="tna_meta_box_nonce" value="', wp_create_nonce(basename(__FILE__)), '" />';
    echo '<table class="form-table">';
    foreach ($meta_box['fields'] as $field) {
        // get current post meta data
        $meta = get_post_meta($post->ID, $field['id'], true);
        echo '<tr>',
        '<th style="width:20%"><label for="', $field['id'], '">', $field['name'], '</label></th>',
        '<td>';
        switch ($field['type']) {
            case 'text':
                echo '<input type="text" name="', $field['id'], '" id="', $field['id'], '" value="', $meta ? $meta : $field['std'], '" size="30" style="width:97%" />', '<br />', $field['desc'];
                break;
            case 'textarea':
                echo '<textarea name="', $field['id'], '" id="', $field['id'], '" cols="60" rows="4" style="width:97%">', $meta ? $meta : $field['std'], '</textarea>', '<br />', $field['desc'];
                break;
            case 'text':
                echo '<input type="text" name="', $field['id'], '" class="', $field['class'], '" id="', $field['id'], '" value="', $meta ? $meta : $field['std'], '" size="30" style="width:97%" />', '<br />', $field['desc'];
                break;
        }
        echo '</td><td>',
        '</td></tr>';
    }
    echo '</table>';
}
