function pdf_link_save( $post_id ) {
    if ( defined( 'DOING_AUTOSAVE' ) && DOING_AUTOSAVE ) return;
    if ( ! isset( $_POST['pdf_link_nonce'] ) || ! wp_verify_nonce( $_POST['pdf_link_nonce'], '_pdf_link_nonce' ) ) return;
